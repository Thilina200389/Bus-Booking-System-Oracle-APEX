
  CREATE TABLE "BUSES" 
   (	"BUS_ID" NUMBER GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"BUS_NUMBER" VARCHAR2(20) NOT NULL ENABLE, 
	"BUS_NAME" VARCHAR2(100) NOT NULL ENABLE, 
	"ROUTE_FROM" VARCHAR2(100) NOT NULL ENABLE, 
	"ROUTE_TO" VARCHAR2(100) NOT NULL ENABLE, 
	"DEPARTURE_TIME" VARCHAR2(5) NOT NULL ENABLE, 
	"CAPACITY" NUMBER(3,0) NOT NULL ENABLE, 
	"TYPE" VARCHAR2(30), 
	"DEPARTURE_DATE" DATE NOT NULL ENABLE, 
	"STATUS" VARCHAR2(20) DEFAULT 'Active', 
	 PRIMARY KEY ("BUS_ID")
  USING INDEX  ENABLE, 
	 UNIQUE ("BUS_NUMBER")
  USING INDEX  ENABLE
   ) ;